# For Loop   (we can use else in for loop)

for i in range(5):
    print(i)
else:
    print("sorry no element remaining ") # In case of break in loop than this won't execute.



# while loop  (we can use else in while loop)
while i < 6:
    print(i)
    i += 1
else:
    print("sorry no element remaining ") # In case of break in loop than this won't execute.



